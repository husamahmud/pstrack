const prisma = new PrismaClient()

async function main() {
  await prisma.$transaction([
    prisma.submissions.deleteMany(),
    prisma.leetcoders.deleteMany(),
    prisma.group_progress.deleteMany(),
    prisma.roadmap.deleteMany(),
    prisma.groups.deleteMany(),
  ])

  await Promise.all([
    prisma.groups.createMany({ data: groups }),
    prisma.roadmap.createMany({ data: roadmap }),
  ]).then(() =>
    Promise.all([
      prisma.leetcoders.createMany({ data: leetcoders }),
      prisma.group_progress.createMany({ data: group_progress }),
      prisma.submissions.createMany({ data: submissions }),
    ])
  )
}

main()
  .then(async () => {
    await prisma.$disconnect()
  })
  .catch(async (e) => {
    console.error(e)
    await prisma.$disconnect()
    process.exit(1)
  })
